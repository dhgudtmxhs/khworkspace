--8
SELECT CLASS_NAME, PROFESSOR_NAME
FROM TB_CLASS
JOIN TB_CLASS_PROFESSOR USING(CLASS_NO)
JOIN TB_PROFESSOR USING (PROFESSOR_NO); 
--바로 JOIN TB_PROFESSOR USING(DEPARTMENT_NO) 하면 
--DEPARTMENT_NO가 001이 2명이라 이상하게뜸 임희도랑 김선진 PROFESSOR_NO이 다름

SELECT CLASS_NAME, PROFESSOR_NAME
FROM TB_CLASS A,  TB_CLASS_PROFESSOR B, TB_PROFESSOR C
WHERE A.CLASS_NO = B.CLASS_NO
AND B.PROFESSOR_NO = C.PROFESSOR_NO;

-- 같은 컬럼 값을 가지는 DEPARTMENT_NO가 많이 존재하는 경우
-- TB_CLASS의 DEPARTMENT_NO와
-- TB_PROFESSOR의 DEPARTMENT_NO 컬럼 값들이
-- 서로 연결되기 위한 모든 경우의 수를 만들어낸다.


--9
SELECT CLASS_NAME , PROFESSOR_NAME
FROM TB_CLASS A
JOIN TB_CLASS_PROFESSOR USING(CLASS_NO)
JOIN TB_PROFESSOR USING (PROFESSOR_NO) 
WHERE A.DEPARTMENT_NO BETWEEN '001' AND '021';

--ORACLE
SELECT CLASS_NAME, PROFESSOR_NAME
FROM TB_CLASS A, TB_CLASS_PROFESSOR B, TB_PROFESSOR C
WHERE A.CLASS_NO = B.CLASS_NO
AND B.PROFESSOR_NO = C.PROFESSOR_NO
AND A.DEPARTMENT_NO BETWEEN '001' AND '021';

SELECT CLASS_NAME, PROFESSOR_NAME
FROM TB_CLASS A, TB_CLASS_PROFESSOR B, TB_PROFESSOR C, TB_DEPARTMENT D
WHERE A.CLASS_NO = B.CLASS_NO
AND B.PROFESSOR_NO = C.PROFESSOR_NO
AND C.DEPARTMENT_NO = D.DEPARTMENT_NO
AND CATEGORY = '인문사회';

