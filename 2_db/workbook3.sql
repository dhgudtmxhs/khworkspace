SELECT STUDENT_NO, STUDENT_NAME
FROM (SELECT STUDENT_NO, STUDENT_NAME FROM TB_STUDENT
        JOIN TB_DEPARTMENT USING (DEPARTMENT_NO)
        JOIN TB_GRADE USING (STUDENT_NO)
        WHERE DEPARTMENT_NAME = '국어국문학과'
        GROUP BY STUDENT_NO, STUDENT_NAME
        ORDER BY AVG(POINT) DESC)
        WHERE ROWNUM = 1;
        
SELECT STUDENT_NO, STUDENT_NAME
FROM 
(SELECT STUDENT_NO, STUDENT_NAME, AVG(POINT) 평점
        FROM TB_GRADE 
        JOIN TB_STUDENT USING(STUDENT_NO)
        WHERE DEPARTMENT_NO = (SELECT DEPARTMENT_NO FROM TB_DEPARTMENT WHERE DEPARTMENT_NAME = '국어국문학과')
        GROUP BY STUDENT_NO, STUDENT_NAME
        ORDER BY 평점 DESC)
        WHERE ROWNUM = 1;

 SELECT ROWNUM, STUDENT_NO, STUDENT_NAME FROM TB_STUDENT 
    JOIN TB_DEPARTMENT USING(DEPARTMENT_NO) 
    JOIN TB_GRADE USING(STUDENT_NO) 
    WHERE ROWNUM = 1 AND DEPARTMENT_NAME = '국어국문학과' 
    GROUP BY ROWNUM, STUDENT_NO, STUDENT_NAME 
    ORDER BY AVG(POINT) DESC; -- ORDER BY 전에 ROWNUM = 1이 되어버림

